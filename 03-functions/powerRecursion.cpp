#include <iostream>  // Бібліотека для вводу/виводу
using namespace std;

// Рекурсивна функція для обчислення n у степені m (n^m)
// unsigned long - беззнаковий довгий цілий тип (тільки додатні числа)
unsigned long pwr(unsigned long n, unsigned long m) {
  // Базовий випадок: будь-яке число в степені 0 дорівнює 1
  if (m == 0) n = 1;
  // Базовий випадок: будь-яке число в степені 1 дорівнює самому собі
  if (m == 1) n = n;
  // Рекурсивний випадок: використовуємо властивість n^m = (n^(m/2))^2
  // m / 2 - цілочисельне ділення (для unsigned long)
  // Алгоритм ділить степінь навпіл, що зменшує кількість множень
  n = pwr(n, m / 2) * pwr(n, m / 2);
  return n;
}

int main() {
  // Ініціалізація змінних через конструктор: unsigned long n(2) еквівалентно unsigned long n = 2
  unsigned long n(2), m(16);  // Обчислюємо 2^16
  cout << pwr(n, m);
  return 0;
}
