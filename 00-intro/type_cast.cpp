/*
 * Демонстрація неявного приведення типів (TypeCast.cpp)
 */
#include <iostream>  // Бібліотека для вводу/виводу
using namespace std; // Використання стандартного простору імен

int main() {
  int i = 1;  // Оголошення та ініціалізація змінної типу int значенням 1
  int d = 3;  // Оголошення та ініціалізація змінної типу int значенням 3

  cout << "i=" << i << endl;
  cout << "d=" << d << endl;
  // Приведення типів: i * 1.0 перетворює int на double (1.0 - дробове число)
  // Результат ділення double / int буде типу double, що дозволяє отримати дробовий результат
  // Без 1.0 результат i / d був би цілочисельним діленням (0 замість 0.333...)
  cout << i * 1.0 / d << endl;
  return 0;  // Успішне завершення програми
}
