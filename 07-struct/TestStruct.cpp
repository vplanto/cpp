/* Тестування структури (TestStruct.cpp) */
#include <iostream>
using namespace std;
 
// struct - користувацький тип даних, що групує змінні різних типів
struct Point {
   int x;  // Поле (член) структури
   int y;  // Поле структури
};
 
int main() {
   // Оголошення та ініціалізація структури через список ініціалізації
   Point p1 = {3, 4};  // Оголошення та ініціалізація полів
   // Доступ до полів структури через оператор . (крапка)
   cout << "(" << p1.x << "," << p1.y << ")" << endl;  // (3,4)
 
   // Порожня ініціалізація - всі поля ініціалізуються значеннями за замовчуванням
   Point p2 = {};      // Оголошення та ініціалізація значеннями за замовчуванням
   cout << "(" << p2.x << "," << p2.y << ")" << endl;  // (0,0)
   // Присвоєння значень полям структури
   p2.x = 7;
   p2.y = 8;
   cout << "(" << p2.x << "," << p2.y << ")" << endl;  // (7,8)
   return 0;
}
