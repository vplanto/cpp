/* Тестування багатовимірного масиву (Test2DArray.cpp) */
#include <iostream>  // Бібліотека для вводу/виводу
using namespace std;
// Прототип функції: void - функція нічого не повертає
// const int[][3] - параметр-масив з фіксованим другим виміром (3 стовпці)
// Тільки перший вимір може бути опущений у параметрі функції
void printArray(const int[][3], int);

int main() {
  // Оголошення двовимірного масиву з опущеним першим виміром
  // Розмір першого виміру визначається автоматично з кількості ініціалізаторів (2 рядки)
  int myArray[][3] = {
      {8, 2, 4}, {7, 5, 2}}; // Ініціалізація масиву 2x3
                             // Тільки перший індекс може бути опущений та визначений автоматично
  printArray(myArray, 2);  // Виклик функції для виведення масиву
  return 0;
}

// Функція для виведення вмісту масиву з rows рядками та 3 стовпцями (кількість стовпців фіксована)
// const - параметр не може бути змінений всередині функції
void printArray(const int array[][3], int rows) {
  // Зовнішній цикл проходить по рядках
  for (int i = 0; i < rows; ++i) {
    // Внутрішній цикл проходить по стовпцях (завжди 3)
    for (int j = 0; j < 3; ++j) {
      cout << array[i][j] << " ";  // Виведення елемента з пробілом
    }
    cout << endl;  // Перехід на новий рядок після виведення рядка масиву
  }
}
