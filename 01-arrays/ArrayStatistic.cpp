#include <iostream>  // Бібліотека для вводу/виводу
using namespace std; // Використання стандартного простору імен

int main() {
  // Оголошення та ініціалізація масиву з 10 цілих чисел
  // Масив - це структура даних, що зберігає послідовність елементів одного типу
  // arr[10] означає масив з 10 елементів, індекси від 0 до 9
  int arr[10] = {23, 45, 12, 67, 34, 89, 21, 90, 76, 50};

  cout << "Please enter array" << endl;
  // Цикл for для введення значень у масив
  // i - індекс елемента масиву (починається з 0)
  for (int i = 0; i < 10; i++) {
    cout << "enter index=" << i << endl;
    cin >> arr[i];  // arr[i] - доступ до елемента масиву за індексом i
  }

  // Змінна для зберігання максимального значення, ініціалізована першим елементом масиву
  int max = arr[0];  // arr[0] - перший елемент масиву (індекс 0)

  // Змінна для зберігання мінімального значення, ініціалізована першим елементом масиву
  int min = arr[0];

  // Змінна для зберігання суми всіх елементів, ініціалізована першим елементом
  int sum = arr[0];

  // Прохід по всіх елементах масиву, починаючи з другого (індекс 1)
  for (int i = 1; i < 10; i++) {
    // Якщо поточний елемент більший за max, оновлюємо max
    if (arr[i] > max) {
      max = arr[i];
    }

    // Якщо поточний елемент менший за min, оновлюємо min
    if (arr[i] < min) {
      min = arr[i];
    }

    // Додаємо поточний елемент до суми
    sum += arr[i];  // Оператор += додає arr[i] до sum
  }

  // Обчислення середнього значення елементів
  // static_cast<double> - явне приведення типу int до double для дробового ділення
  double average = static_cast<double>(sum) / 10.0;

  // Виведення максимального значення
  cout << "Maximum value in the array: " << max << endl;

  // Виведення мінімального значення
  cout << "Minimum value in the array: " << min << endl;

  // Виведення середнього значення
  cout << "Average value of the array elements: " << average << endl;

  return 0;  // Успішне завершення програми
}
